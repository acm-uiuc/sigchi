			<h3>About Us</h3>
			<p>
				SIGCHI is the Special Interest Group for Computer-Human Interaction.  We are
				the student chapter at the University of Illinois at Urbana-Champaign.  Our
				purpose is twofold: to investigate how technology can be made easier to use,
				and to explore the strengths and weaknesses of different interface paradigms.
				This means we are interested in everything from how to make usable software
				for touch screens, to how different menu shapes can affect navigation time.
			</p>
